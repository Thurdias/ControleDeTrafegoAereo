
public class Aviao {
	// Propriedades da classe
	private int x = 0;
	private int y = 0;
	
	// Métodos construtores da classe
	public Aviao() {
		super();
	}

	public Aviao(int x, int y) {
		super();
		this.x = x;
		this.y = y;
	}

	// Métodos get/set da classe
	public int getX() {
		return x;
	}

	public void setX(int x) {
		this.x = x;
	}

	public int getY() {
		return y;
	}

	public void setY(int y) {
		this.y = y;
	}
}